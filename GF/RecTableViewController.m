@implementation RecTableViewController

- (id)initWithStyle:(UITableViewStyle)style andRec:(NSArray *)foodRecs {
    self = [super initWithStyle:style];
    if (self){
        self.foodRecs = foodRecs;
        self.foodSelected = [[NSMutableArray alloc] init];
    }
    return self;
}

