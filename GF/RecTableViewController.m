    NSLog(@"Food selected: %@", self.foodSelected);
}

- (void) tableView:(UITableView *)tableView didDeselectRowAtIndexPath:(NSIndexPath *)indexPath {
    [self.foodSelected removeObject:[self.foodRecs objectAtIndex:indexPath.row]];
    NSLog(@"Food selected: %@", self.foodSelected);
    
}


